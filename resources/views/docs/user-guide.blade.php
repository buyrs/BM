## Getting Started

### Account Setup

1. **Receive Invitation**: You will receive an email invitation with your login credentials
2. **First Login**: Navigate to your role-specific login page:
   - Administrators: [Admin Portal](/admin/login)
   - Operations Staff: [Ops Portal](/ops/login)
   - Property Checkers: [Checker Portal](/checker/login)
3. **Change Password**: For security, you'll be prompted to change your password on first login
4. **Complete Profile**: Update your profile information as needed

### Navigation

The platform features intuitive navigation based on your role:

- **Desktop**: Sidebar navigation with quick access to all features
- **Mobile**: Bottom navigation bar for easy access on smaller screens
- **Search**: Global search functionality available in the header

### Dashboard Overview

Upon login, you'll see your role-specific dashboard with:
- Quick overview of your responsibilities
- Recent activity and notifications
- Shortcuts to commonly used features

## Administrator Guide

### User Management

**Creating Users:**
1. Navigate to **Admin > Users > Create User**
2. Fill in user details (name, email, role)
3. Set initial password or let system generate one
4. User receives email with login instructions

**Editing Users:**
1. Go to **Admin > Users**
2. Click on the user you want to edit
3. Update information as needed
4. Save changes

**Deleting Users:**
1. Select user from the users list
2. Click **Delete** and confirm
3. Note: This action cannot be undone

### Mission Oversight

Administrators have full visibility into all missions and can:
- Approve or reject mission requests
- Assign missions to different operations staff
- View mission progress and completion status
- Generate reports on mission performance

### System Configuration

**Settings Management:**
- Configure platform-wide settings
- Manage property types and categories
- Set up notification preferences
- Configure email templates

**Security Settings:**
- Monitor user activity logs
- Review failed login attempts
- Manage two-factor authentication settings
- Audit permission changes

## Operations Staff Guide

### Mission Creation

**Creating New Missions:**
1. Navigate to **Ops > Missions > Create Mission**
2. Enter mission details:
   - Title and description
   - Property address
   - Check-in and check-out dates
   - Assigned checker
3. Submit for administrator approval

**Editing Missions:**
- Modify mission details before approval
- Update dates or assigned personnel
- Add notes or special instructions

### Property Management

**Adding Properties:**
1. Go to **Ops > Properties > Add Property**
2. Enter complete property information:
   - Full address
   - Property type
   - Owner contact information
   - Amenities list
3. Upload property photos

**Managing Property Lists:**
- Import multiple properties via CSV
- Export property lists for reporting
- Update property statuses
- Archive inactive properties

### Checker Management

**Assigning Checkers:**
- View checker availability and workload
- Assign checkers to specific missions
- Transfer assignments between checkers
- Monitor checker performance metrics

## Property Checker Guide

### Checklist Access

Upon assignment to a mission, you'll see checklists appear in your dashboard:
- Check-in checklist (created when mission is assigned)
- Check-out checklist (created closer to checkout date)

### Completing Checklists

**Accessing Checklists:**
1. Navigate to **Checker > Dashboard**
2. Click on the mission you want to work on
3. Select the appropriate checklist (check-in or check-out)

**Filling Checklist Items:**
1. Review each checklist item carefully
2. Select the appropriate condition state:
   - Bad
   - Average
   - Good
   - Excellent
   - Need a Fix
3. Add comments for any items requiring explanation
4. Upload photos for damaged or problematic items

**Saving Progress:**
- Use **Save Checklist** to save your work without submitting
- This allows you to return later to complete the checklist

**Submitting Checklists:**
1. Ensure all items are completed
2. Review your entries for accuracy
3. Click **Submit Checklist**
4. Add any final notes if needed

### Photo Requirements

**Taking Quality Photos:**
- Ensure good lighting
- Capture the entire item being documented
- Include close-ups of damage
- Use the platform's built-in camera feature when available

**Photo Management:**
- Photos are automatically compressed for storage efficiency
- You can delete and retake photos before submission
- All photos are timestamped and geotagged

## Mission Management

### Mission Lifecycle

**1. Creation:**
- Operations staff creates mission request
- Includes all mission details and requirements

**2. Approval:**
- Administrator reviews and approves mission
- Assignment confirmed with checker

**3. Execution:**
- Property checker completes required checklists
- Progress tracked in real-time

**4. Completion:**
- All checklists submitted
- Mission marked as complete
- Reports generated automatically

### Mission Status Tracking

**Pending:** Waiting for administrator approval
**Approved:** Approved and assigned to checker
**In Progress:** Checker has started working on checklists
**Completed:** All checklists submitted and reviewed
**Cancelled:** Mission was cancelled before completion

### Calendar Integration

The platform includes a calendar view showing:
- Upcoming missions
- Mission deadlines
- Assignment schedules
- Conflict detection for overlapping dates

## Checklist System

### Checklist Types

**Standard Checklists:**
- Pre-defined templates for different property types
- Customizable sections based on property amenities

**Custom Checklists:**
- Created for unique property requirements
- Saved as templates for future use

### Checklist Items

Each checklist item includes:
- **Amenity:** The specific item being inspected
- **Condition:** State rating (Bad to Excellent)
- **Comments:** Notes about the item's condition
- **Photos:** Visual documentation of condition
- **Signature:** Tenant acknowledgment (for check-out)

### Quality Assurance

**Validation Rules:**
- All required items must be completed
- Condition ratings must be selected
- Comments required for poor conditions
- Photos mandatory for damaged items

**Review Process:**
- Submitted checklists undergo quality review
- Incomplete items flagged for correction
- Notifications sent for required revisions

