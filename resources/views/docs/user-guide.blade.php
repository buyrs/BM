## FAQ

**Q: How do I reset my password?**
A: Use the "Forgot Password" link on your login page and follow the email instructions.

**Q: Can I access the platform on mobile devices?**
A: Yes, the platform is fully responsive and works on smartphones and tablets.

**Q: What file types can I upload for photos?**
A: JPG, PNG, and GIF files are supported. Maximum file size is 10MB.

**Q: How often is my data backed up?**
A: Data is backed up daily with point-in-time recovery available for the last 30 days.

**Q: Is there a mobile app available?**
A: The platform is a Progressive Web App (PWA) that can be installed on your mobile device for app-like experience.

**Q: How do I report a bug or suggest a feature?**
A: Contact our support team at support@bailmobilite.com with details about the issue or suggestion.

