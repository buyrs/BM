<p>RESTful API for Property Management System with mission tracking, checklists, and file management.</p>
<aside>
    <strong>Base URL</strong>: <code>http://localhost</code>
</aside>
<pre><code>This documentation provides comprehensive information for working with the Property Management API.

## Authentication
This API uses Bearer token authentication. Include your token in the Authorization header:
`Authorization: Bearer {your-token}`

## Rate Limiting
API requests are rate limited. Check the response headers for current limits and remaining requests.

## Versioning
The API is versioned using URL prefixes. Current version is v1: `/api/v1/`

&lt;aside&gt;As you scroll, you'll see code examples for working with the API in different programming languages in the dark area to the right (or as part of the content on mobile).&lt;/aside&gt;</code></pre>

